// Using the Unary Plus (+) Operator before string variable
let x = '4';
console.log(+x); //4
console.log(2 + +x); //6
console.log(Number(x)); // 4
console.log(parseInt(x)); // 4

// Using the built-in Number Constructor Function (The most direct way) 
const str = "10";
const number = Number(str);
console.log(number); // 10

